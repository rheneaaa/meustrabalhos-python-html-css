var n2 = 5

if (n1 == n2) {
    document.write('<h2>Operador de comparação: ' + n1 + ' é igual a ' + n2 + '</h2><br><br>')
}else {
    document.write('<h2>Operador de comparação: ' + n1 + ' não é igual a n2 ' + '</h2><br><br>')
}

if (n1 != n2) {
    document.write('<h2>O valor é diferente</h2><br><br>')
}else {
    document.write('<h2>O valor é igual</h2><br><br>')
}

if (n1 < n2) {
    document.write('<h2>O n1 é menor que n2</h2><br><br>')
}else {
    document.write('<h2>O n1 é maior que n2</h2><br><br>')
}

if (n1 > n2) {
    document.write('<h2>O n1 é maior que n2</h2><br><br>')
}else {
    document.write('<h2>O n1 é menor que n2</h2><br><br>')
}

if (n1 >= n2) {
    document.write('<h2>O valor de n1 é maior ou igual a n2</h2><br><br>')
}else {
    document.write('<h2>O valor de n1 não é nem maior e nem igual a n2</h2><br><br>')
}

